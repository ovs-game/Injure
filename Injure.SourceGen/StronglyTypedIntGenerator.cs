	// ==========================================================================
	// IIncrementalGenerator
	public void Initialize(IncrementalGeneratorInitializationContext context) {
		context.RegisterPostInitializationOutput(static (IncrementalGeneratorPostInitializationContext ctx) =>
			ctx.AddSource("StronglyTypedIntAttribute.g.cs", SourceText.From(AttributeSource, Encoding.UTF8))
		);
		IncrementalValuesProvider<TargetInfo> targets = context.SyntaxProvider.ForAttributeWithMetadataName(
			AttributeMetadataName,
			predicate: static (SyntaxNode node, CancellationToken _) => node is StructDeclarationSyntax,
			transform: check
		);
		context.RegisterSourceOutput(targets.Collect(), static (SourceProductionContext ctx, ImmutableArray<TargetInfo> infos) => {
			HashSet<INamedTypeSymbol> seen = new HashSet<INamedTypeSymbol>(SymbolEqualityComparer.Default);
			foreach (TargetInfo info in infos) {
				foreach (Diagnostic diag in info.Diagnostics)
					ctx.ReportDiagnostic(diag);
				if (!info.ShouldEmit || !seen.Add(info.Symbol))
					continue;
				ctx.AddSource(getHintName(info), SourceText.From(gen(info), Encoding.UTF8));
			}
		});
	}

	private static string getHintName(TargetInfo info) =>
		(info.Namespace is not null ? info.Namespace + "." + info.Symbol.Name : info.Symbol.Name) + GeneratedSourceSuffix;

	private static TargetInfo check(GeneratorAttributeSyntaxContext ctx, CancellationToken ct) {
		INamedTypeSymbol sym = (INamedTypeSymbol)ctx.TargetSymbol;
		Location loc = ctx.Attributes[0].ApplicationSyntaxReference?.GetSyntax(ct).GetLocation() ?? ctx.TargetNode.GetLocation();
		if (sym.TypeKind != TypeKind.Struct)
			return TargetInfo.Invalid(sym, Diagnostic.Create(InvalidTarget, loc, "Target must be a struct."));
		if (!partial(sym, ct))
			return TargetInfo.Invalid(sym, Diagnostic.Create(InvalidTarget, loc, "Target must be declared 'partial'."));
		if (!sym.IsReadOnly)
			return TargetInfo.Invalid(sym, Diagnostic.Create(MustBeReadonly, loc, sym.Name));
		if (sym.ContainingType is not null)
			return TargetInfo.Invalid(sym, Diagnostic.Create(InvalidTarget, loc, "Nested structs are not supported."));
		if (sym.TypeParameters.Length != 0)
			return TargetInfo.Invalid(sym, Diagnostic.Create(InvalidTarget, loc, "Generic structs are not supported."));
		if (sym.IsRecord)
			return TargetInfo.Invalid(sym, Diagnostic.Create(InvalidTarget, loc, "'record struct' is not supported."));

		AttributeData attr = ctx.Attributes[0];
		if (attr.ConstructorArguments.Length != 1)
			return TargetInfo.Invalid(sym, Diagnostic.Create(InvalidTarget, loc, "Attribute must have exactly one typeof(...) argument."));
		if (attr.ConstructorArguments[0].Value is not INamedTypeSymbol backingType)
			return TargetInfo.Invalid(sym, Diagnostic.Create(InvalidTarget, loc, "Attribute argument must be a concrete type."));
		if (!tryGetBackingInfo(backingType, out string? backingTypeName, out bool isSigned))
			return TargetInfo.Invalid(sym, Diagnostic.Create(UnsupportedBacking, loc, backingType.ToDisplayString()));
		if (checkCollision(sym, backingType, out Location collisionLoc, out string? collisionMsg))
			return TargetInfo.Invalid(sym, Diagnostic.Create(MemberCollision, collisionLoc, collisionMsg));
		string? ns = !sym.ContainingNamespace.IsGlobalNamespace ? sym.ContainingNamespace.ToDisplayString() : null;
		return TargetInfo.Valid(sym, ns, backingTypeName, isSigned);
	}

	private static bool partial(INamedTypeSymbol sym, CancellationToken ct) {
		foreach (SyntaxReference sr in sym.DeclaringSyntaxReferences)
			if (sr.GetSyntax(ct) is StructDeclarationSyntax s && s.Modifiers.Any(SyntaxKind.PartialKeyword))
				return true;
		return false;
	}

	private static bool tryGetBackingInfo(INamedTypeSymbol sym, /* [NotNullWhen(true)] */ out string? name, out bool signed) {
		switch (sym.SpecialType) {
		case SpecialType.System_Int32:
			name = "int";
			signed = true;
			return true;
		case SpecialType.System_UInt32:
			name = "uint";
			signed = false;
			return true;
		case SpecialType.System_Int64:
			name = "long";
			signed = true;
			return true;
		case SpecialType.System_UInt64:
			name = "ulong";
			signed = false;
			return true;
		}
		if (sym.ContainingNamespace.ToDisplayString() == "System") {
			switch (sym.Name) {
			case "Int128":
				name = "global::System.Int128";
				signed = true;
				return true;
			case "UInt128":
				name = "global::System.UInt128";
				signed = false;
				return true;
			}
		}

		name = null;
		signed = false;
		return false;
	}

	private static bool checkCollision(INamedTypeSymbol sym, INamedTypeSymbol backingType, out Location loc, /* [NotNullWhen(true)] */ out string? msg) {
		ImmutableArray<ISymbol> members = sym.GetMembers(BackingFieldName);
		if (members.Length != 0) {
			loc = getLocation(members[0], sym);
			msg = $"Type '{sym.Name}' already contains a member named '{BackingFieldName}', which is reserved by StronglyTypedInt.";
			return true;
		}
		foreach (IMethodSymbol ctor in sym.InstanceConstructors) {
			if (ctor.IsImplicitlyDeclared || ctor.Parameters.Length != 1 ||
				SymbolEqualityComparer.Default.Equals(ctor.Parameters[0].Type, backingType))
				continue;
			loc = getLocation(ctor, sym);
			msg = $"Type '{sym.Name}' already contains a constructor with signature '({backingType.ToDisplayString()})', which conflicts with generated code.";
			return true;
		}
		loc = sym.Locations.FirstOrDefault(static (Location l) => l.IsInSource) ?? Location.None;
		msg = null;
		return false;
	}

	private static Location getLocation(ISymbol sym, INamedTypeSymbol fallback) {
		return sym.Locations.FirstOrDefault(static (Location l) => l.IsInSource) ??
			fallback.Locations.FirstOrDefault(static (Location l) => l.IsInSource) ??
			Location.None;
	}

