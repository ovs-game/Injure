	// internal types
	private sealed class TargetInfo(INamedTypeSymbol symbol, string? ns, string? backingTypeName, bool isSigned, ImmutableArray<Diagnostic> diagnostics) {
		public INamedTypeSymbol Symbol { get; } = symbol;
		public string? Namespace { get; } = ns;
		public string? BackingTypeName { get; } = backingTypeName;
		public bool IsSigned { get; } = isSigned;
		public ImmutableArray<Diagnostic> Diagnostics { get; } = diagnostics;
		public bool ShouldEmit => Diagnostics.IsDefaultOrEmpty || Diagnostics.Length == 0;

		public static TargetInfo Valid(INamedTypeSymbol symbol, string? ns, string? backingTypeName, bool isSigned) =>
			new TargetInfo(symbol, ns, backingTypeName, isSigned, ImmutableArray<Diagnostic>.Empty);
		public static TargetInfo Invalid(INamedTypeSymbol symbol, Diagnostic diagnostic) =>
			new TargetInfo(symbol, null, null, false, ImmutableArray.Create(diagnostic));
